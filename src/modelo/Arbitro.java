package modelo;

public class Arbitro {
	private String nombre;
	private int codigoA;
	
	// getters and setters
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getCodigoA() {
		return codigoA;
	}
	public void setCodigoA(int codigoA) {
		this.codigoA = codigoA;
	}
	
	
}
